  function updatePassword($currentPassword, $newPassword, $confirmNewPassword) {
    global $conn;

    if (!isset($_SESSION['user_id'])) {
        return "You must be logged in to update your password.";
    }

    $userId = $_SESSION['user_id'];


    // Retrieve the current password hash from the database
    $sql = "SELECT password FROM users WHERE id = '$userId'";
    $result = mysqli_query($conn, $sql);

    if (!$result || mysqli_num_rows($result) === 0) {
        return "User not found.";
    }

    $row = mysqli_fetch_assoc($result);
    $hashedPassword = $row['password'];

    // Verify the current password
    if (!password_verify($currentPassword, $hashedPassword)) {
        return "The current password is incorrect.";
    }

    // Validate new password and confirm password
    if (empty($newPassword) || empty($confirmNewPassword)) {
        return "Both new password and confirm password are required.";
    }

    if ($newPassword !== $confirmNewPassword) {
        return "New password and confirm password do not match.";
    }

    // Hash the new password
    $newHashedPassword = password_hash($newPassword, PASSWORD_DEFAULT);

    // Update the password in the database
    $updateSql = "UPDATE users SET password = '$newHashedPassword' WHERE id = '$userId'";
    if (mysqli_query($conn, $updateSql)) {
        return "Password updated successfully.";
    } else {
        return "Error updating password: " . mysqli_error($conn);
    }
}



